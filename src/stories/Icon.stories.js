const createIconGallery = (options, type, size = 24) => {
  const entries = Object.entries(options);
  const sizeClass = SIZE_MAP[size] || `-x${size}`;
  const gridId = `icon-grid-${type}`;

  const cardsHtml = entries.map(([label, value]) => {
    const isGeneral = type === 'General';
    
    const attrs = [
      `class="nds-ui nds-ico ${sizeClass}${isGeneral ? ` ${value}` : ''}"`,
      `aria-hidden="true"`
    ];

    if (!isGeneral) {
      attrs.push(type === 'Flag' ? `data-countrycode="${value}"` : `data-bankcode="${value}"`);
    }

    const fullTag = `<i ${attrs.join(' ')}></i>`;

    const safeClipboardText = fullTag.replace(/"/g, '&quot;');

    return `
      <div class="sb-gallery-card">
        <div class="sb-hover-effect" 
             style="cursor: pointer;"
             title="클릭하여 HTML 복사"
             onclick="
               navigator.clipboard.writeText('${safeClipboardText}').then(() => {
                 const old = document.querySelector('.sb-toast-message');
                 if (old) old.remove();

                 const toast = document.createElement('div');
                 toast.className = 'sb-toast-message';

                 Object.assign(toast.style, {
                   position: 'fixed',
                   bottom: '20px',
                   left: '50%',
                   transform: 'translateX(-50%)',
                   backgroundColor: '#333333',
                   color: '#ffffff',
                   padding: '12px 24px',
                   borderRadius: '8px',
                   fontSize: '14px',
                   fontWeight: 'bold',
                   zIndex: '99999',
                   boxShadow: '0 4px 12px rgba(0,0,0,0.15)',
                   opacity: '0',
                   transition: 'opacity 0.3s ease, transform 0.3s ease',
                   pointerEvents: 'none'
                 });

                 toast.innerHTML = '✅ 복사되었습니다!';
                 document.body.appendChild(toast);

                 requestAnimationFrame(() => {
                   toast.style.opacity = '1';
                   toast.style.transform = 'translate(-50%, -10px)';
                 });

                 setTimeout(() => {
                   toast.style.opacity = '0';
                   toast.style.transform = 'translate(-50%, 0)';
                   setTimeout(() => toast.remove(), 300);
                 }, 2000);
               });
             ">
          ${fullTag}
        </div>
        <div class="sb-card-footer">
          <span class="sb-label">${label}</span>
          <code class="sb-code">${value}</code>
        </div>
      </div>
    `;
  }).join('');

  return `
    <div class="sb-gallery-wrap">
      <div id="${gridId}" class="sb-grid">
        ${cardsHtml}
      </div>
    </div>
  `;
};

